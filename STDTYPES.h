typedef  signed char sint8;
typedef unsigned short int uint16;
typedef  short int sint16;
typedef long unsigned   uint32;
typedef long  sint32;
typedef long long unsigned   uint64;
typedef long long   sint64;

#endif /* STDTYPES_H_ */
